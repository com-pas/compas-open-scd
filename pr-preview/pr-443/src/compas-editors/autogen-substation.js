      iedName: ln.parentElement?.parentElement?.parentElement?.parentElement?.getAttribute("name") ?? null,
      ldInst: cswi.parentElement?.getAttribute("inst") ?? null,
      prefix: ln.getAttribute("prefix"),
      lnClass: ln.getAttribute("lnClass"),
